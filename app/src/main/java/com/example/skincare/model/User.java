package com.example.skincare.model;

public class User {

        public int id;
        public String username;
        public String email;
        public String gender;
        public int age;

        public String getUsername() {
            return username;
        }

        public String getEmail() {
            return email;
        }

        public String getGender() {
            return gender;
        }

        public int getAge() {
            return age;
        }


}
